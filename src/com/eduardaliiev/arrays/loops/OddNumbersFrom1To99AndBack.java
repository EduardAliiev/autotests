package com.eduardaliiev.arrays.loops;

import java.util.Scanner;

public class OddNumbersFrom1To99AndBack {
    public static void calculateLoopNumbers() {
        Scanner scanner = new Scanner (System.in);
        for (int A = 1; A <= 99; A++) {
            System.out.print (A * 1 + " ");
        }
    }
}